public class RenombrarVariables {
    //psvm
    public static void main(String[] args) {
        var nombre = "Jorge";
        var edad = 48;
        var estatura = 1.80;

        System.out.println("nombre = " + nombre);
        System.out.println("edad = " + edad);
        System.out.println("estatura = " + estatura);

        nombre = "Andres";
        edad = 47;
        estatura = 1.78;

        System.out.printf("\n");
        System.out.println("nombre = " + nombre);
        System.out.println("edad = " + edad);
        System.out.println("estatura = " + estatura);





    }


}
