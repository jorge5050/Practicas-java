public class ArregloSimplificado {
    public static void main(String[] args) {
        System.out.println("*** Arreglo Simplificado ***");
         int numeros[] = new int[]{100, 200, 400, 500, 600};

         for (var i= 0; i < numeros.length; i++){
             System.out.println("posicion de indice = "+ i + " " + numeros[i]);
         }
    }
}
